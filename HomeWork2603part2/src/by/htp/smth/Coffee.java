package by.htp.smth;

public class Coffee {
	private int gramPrice;
	private String state;
	private String sort;
	
	public Coffee(int gramPrice, String state, String sort) {
		this.state = state;
		this.state = state;
		this.sort = sort;
	}

	public int getGramPrice() {
		return gramPrice;
	}

	public String getState() {
		return state;
	}

	public String getSort() {
		return sort;
	}
	
	

}
